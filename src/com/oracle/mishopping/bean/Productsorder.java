package com.oracle.mishopping.bean;


public class Productsorder {

  private long poid;
  private long pid;
  private long oid;
  private long pnum;


  public long getPoid() {
    return poid;
  }

  public void setPoid(long poid) {
    this.poid = poid;
  }


  public long getPid() {
    return pid;
  }

  public void setPid(long pid) {
    this.pid = pid;
  }


  public long getOid() {
    return oid;
  }

  public void setOid(long oid) {
    this.oid = oid;
  }


  public long getPnum() {
    return pnum;
  }

  public void setPnum(long pnum) {
    this.pnum = pnum;
  }

}
